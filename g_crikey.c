void print_crikey_time(edict_t* ent, const char* map) {
    // TODO: currently linearly searching through the list.
    // Can improve by moving to std::map or putting into a tree.
    for (int i = 0; i < NUM_CRIKEY_TIMES; ++i) {
        if (Q_stricmp((char*)map, (char*)crikey_times[i].map) == 0) {
            gi.cprintf(ent, PRINT_HIGH, "Crikey time for map %s: %s in %s seconds\n", crikey_times[i].map, crikey_times[i].player, crikey_times[i].time);
            return;
        }
    }
    // If we get here, we didn't find the map
    gi.cprintf(ent, PRINT_HIGH, "No Crikey! times found for map %s\n", map);
    return;
}
